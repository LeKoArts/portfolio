describe('Navigation English', () => {
  beforeEach(() => {
    cy.visit('/en')
  })
  it('in the header works', () => {
    cy.get('nav').within(() => {
      cy.findByText('Projects')
        .click({ force: true })
        .waitForRouteChange()
        .assertRoute('/en/projects')
    })
    cy.findByText('Blog')
      .click({ force: true })
      .waitForRouteChange()
      .assertRoute('/en/blog')
      .findByText('Contact')
      .click({ force: true })
      .waitForRouteChange()
      .assertRoute('/en/contact')
  })
  it('to internal sites in the footer works', () => {
    cy.findByText('Privacy Policy')
      .click({ force: true })
      .waitForRouteChange()
      .assertRoute('/en/privacy')
      .findByText('Imprint')
      .click({ force: true })
      .waitForRouteChange()
      .assertRoute('/en/imprint')
      .findByText('Freebies')
      .click({ force: true })
      .waitForRouteChange()
      .assertRoute('/en/categories/freebie')
      .findByText('Tutorials')
      .click({ force: true })
      .waitForRouteChange()
      .assertRoute('/en/categories/tutorial')
  })
})
